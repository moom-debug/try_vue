            >&nbsp;&nbsp;&nbsp;&nbsp;第六条&nbsp;实验室应明确分工，指定专人进行共享仪器的操作，以保证仪器的完好和服务的质量。仪器操作人员应保持相对稳定，如有人员变动，由实验室主任负责做交接手续。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第七条&nbsp;操作的应做到：①责任心强，能熟练掌握仪器的操作技术，明确操作规程和注意事项，了解仪器的一般故障的排除和处理方法，能保证操作质量。②能热情为用户服务，耐心细致做好咨询和解释工作，为用户提供时间保障，不得无故拒绝用户使用。③定期作好仪器的维护和保养工作，使仪器经常处于良好的工作状态，如出现问题，及时向实验室主任汇报。④及时编制所需的消耗品和试剂的补充购置计划。⑤及时认真地填写使用记录，记录内容完整、规范，并由操作人员和用户单位人员签名。⑥保持仪器工作环境的整洁有序，能严格执行管理制度。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第八条&nbsp;由于仪器操作人员的主观原因造成的未能按时上机和样品、试剂的浪费应由操作人员和仪器所属单位负责。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;三、用户的规定</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第九条&nbsp;用户需在使用仪器的前3—5天向仪器所在单位提出预约申请，并说明所做实验的要求、所需条件、耗材与试剂等项情况，以便提前做好准备，保证仪器按时使用。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >第十条&nbsp;用户预约后应按时上机，如用户方面原因未能按时上机造成样品、试剂的浪费，应由用户负责。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十一条&nbsp;用户必须经严格培训，熟练掌握操作技能才能独立上机操作，在使用仪器的过程中应遵守仪器所在单位的有关规定，服从仪器操作人员的管理，未经许可不得随意动用实验室内的仪器设备，注意维护实验室的环境卫生和秩序。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十二条&nbsp;实验完成后，做好记录，认真填写用户意见，及时清理所用物品。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;四、收费原则</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十三条&nbsp;收费标准的计算</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;1、额定收费应包括设备折旧费、设备维修费、材料消耗费、水电消耗费、技术服务费和管理费等项内容。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;2、参照广东省大型仪器共用网收费标准，由设备处牵头会同测试中心制定全校统一的收费标准。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;3、校内人员使用仪器享受优惠价格，一般按额定收费标准的50%计费。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;五、收费管理及有关政策</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十四条&nbsp;收费办法</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;1、校外人员的仪器使用费以现金和支票支付，校内人员的仪器使用费以内部转帐支付。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;2、以仪器所属单位实验室出具的仪器使用收费通知为收费依据。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;3、仪器使用费当月结清，年底结帐。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;4、本着责、权、利兼顾，学校仪器所属单位和仪器操作人员利益兼顾的原则，实行收益分成（具体分配办法按学校有关规定办理）。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十五条&nbsp;大型精密贵重仪器资源共享的维持费用</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;1、为推动大型仪器资源共享工作的开展，学校设立专项资金，对开展共享工作的机组其给予一定补贴。大型专用仪器有效使用机时在800小时/年，通用仪器有效使用机时在1400小时/年以上的，由学校每年拨出必要的经费，作为仪器资源共享的运行维修费，此项费用由主管校长掌握，由仪器管理中心报批。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;2、仪器使用所收费用中提取适当比例列入仪器日常维持费，此项费用由仪器所属实验室主任掌握。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;六、资源共享工作的考核、评比</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十六条&nbsp;考评工作的基本原则是坚持“以评促管，以管促效”。通过评估，进一步促进我校大型仪器的使用和管理，发挥其在教学、科研和社会服务中的重要作用，调动各方面积极性，表扬先进，充分发挥挖掘潜力，提高社会效益和经济效益，使大型仪器的使用和管理走上良性循环的轨道。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十七条&nbsp;仪器资源共享工作定期进行考核评比，考核评比的主要内容：</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;1、开展资源共享的有效机时/年（以由用户签名的原始记录和收费凭证为凭据）。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;2、资源共享的规章制度是否健全以及规章制度的落实情况。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;3、分工是否明确，操作人员的服务质量好坏与用户的反映如何。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;4、账目是否有专人管理以及账目管理的情况。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;5、仪器使用保养的状况和实验室的环境。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;6、完成和进行的实验项目、效益和实际收入情况。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十八条&nbsp;考评的主要机构是贵重仪器管理中心处。考评工作每两年进行一次。总结工作，评出资源共享工作的先进实验室和先进个人。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第十九条&nbsp;考评工作的程序</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;1、仪器所述单位对仪器共享进行工作总结，认真总结的基础上组织自查自评；</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;2、有贵重仪器管理中心组织专家进行评审，作出评价并签署意见。仪器共享先进实验室和先进个人由主管校长审批。</span
          >
        </p>
        <p class="p">
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;第二十条&nbsp;奖惩办法</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;1、学校从仪器共享收费中设立大型仪器奖励基金，提取一部分作为对仪器资源共享先进实验室和先进个人的奖励，由主管校长掌握，贵重仪器管理中心负责报批。</span
          >
        </p>
        <p class="p">
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;2、学校为先进实验室和先进个人颁发奖状和证书。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;3、对因操作人员的服务态度、工作质量和操作水平等问题而影响仪器共享工作开展的，要进行批评和帮助，使其尽快达到所要求。如仍不能有效改进的，可进行人员调整；对因责任心不强和造成仪器损坏、丢失的要按有关管理规定处理。</span
          ><br />
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;4、对有条件而不能及时开展仪器资源共享工作，仪器使用率又低于100小时每年的仪器所属单位和实验室，要视情节轻重采取一定处罚措施：①找出原因，制定整改计划，限期改进；②减免校拨专项运行维修费；③在申购仪器设备时从严审批；④对本单位闲置不用又未能投入资源共享的仪器，转交给学校其他部门管理或无偿调转给学校其他实验室使用。第二十一条&nbsp;此办法自批准之日起执行。&nbsp;&nbsp;&nbsp;</span
          >
        </p>
        <p class="p">
          <span style="font-family: SimSun; font-size: 14px"
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第二十二条&nbsp;此办法解释权在设备资产管理处。</span
          >
        </p>
